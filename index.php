<script>
    // Function to load more content
    function loadMoreContent() {
        // Simulated loading delay (you can replace this with an actual AJAX call)
        setTimeout(function() {
            // Example content to be appended
            var moreContent = '<div class="card mb-3"><div class="card-body">More Content</div></div>';
            document.getElementById('content').insertAdjacentHTML('beforeend', moreContent);
            // Hide loading indicator after content is loaded
            //  alert('sasas');
            document.getElementById('loading').style.display = 'none';
        }, 1000); // Simulated delay of 1 second
    }

    // Intersection Observer setup
    var options = {
        root: null,
        rootMargin: '0px',
        threshold: 1.0
    };

    var observer = new IntersectionObserver(function(entries, observer) {
        entries.forEach(function(entry) {
            if (entry.isIntersecting) {
                // Show loading indicator
                document.getElementById('loading').style.display = 'block';
                // Load more content
                loadMoreContent();
            }
        });
    }, options);

    // Start observing the loading indicator
    observer.observe(document.getElementById('loading'));
</script>

